# Задание 1
# Пользователь вводит с клавиатуры номер дня недели
# (1-7). Необходимо вывести на экран названия дня недели. Например, если 1,
# то на экране надпись понедельник, 2 - вторник и тд

num_User = input("Введите номер недели - ")

weeks = ['', 'понедельник', 'вторник', 'среда', 'четверг', 'пятница', 'суббота', 'воскресенье']
print("День недели -", weeks[int(num_User)])

