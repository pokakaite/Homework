# Задание 3
# Пользователь вводит с клавиатуры число. Если число
# больше нуля нужно вывести надпись «Number is positive»,
# если меньше нуля «Number is negative», если равно нулю
# «Number is equal to zero»

num_User = input("Введите любое число - ")

if int(num_User) > 0:
    print("Число", int(num_User), "больше нуля")

if int(num_User) < 0:
    print("Число", int(num_User), "меньше нуля")

if int(num_User) == 0:
    print("Число", int(num_User), "равно нулю")

# Задание 4
# Пользователь вводит два числа. Определить, равны
# ли эти числа, и, если нет, вывести их на экран в порядке
# возрастания.

x = input("Введите число - ")
y = input("Введите число - ")

if int(x) == int(y):
    print("Числа равны")

if int(x) > int(y):
    print(int(y), int(x))

if int(x) < int(y):
    print(int(x), int(y))